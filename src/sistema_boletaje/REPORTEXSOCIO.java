    private void rSButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButton1ActionPerformed
        int fila = tpropietario.getSelectedRow();
        if (fila==-1){
            JOptionPane.showMessageDialog(null,"SELECCIONE UN PROPIETARIO","ERROR",0);
        }else{
            try {
                
                int mes = calendario.currentMonth+1;
                int year = calendario.currentYear;
                
                ConexionBD con = new ConexionBD();
                Connection conn = con.getConexion();
                JasperReport reporte = null;
                //String path ="src\\reportes\\report1.jasper";
                Map parametro = new HashMap();
                parametro.put("mes",mes );
                parametro.put("year", year);
                parametro.put("Propietario", tpropietario.getValueAt(fila, 0).toString());
                parametro.put("nombre",tpropietario.getValueAt(fila,1).toString()+" "+tpropietario.getValueAt(fila,2).toString()+" "+tpropietario.getValueAt(fila,3).toString());
                URL path = this.getClass().getResource("/reportes/REPORTEMENSUALBOLETOxSocio.jasper");
                reporte = (JasperReport) JRLoader.loadObject(path);                
                JasperPrint jprint = JasperFillManager.fillReport(reporte, parametro,conn);
                JasperViewer view = new JasperViewer(jprint,false); 
                view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
                view.setVisible(true);
                
            } catch (JRException ex) {
                Logger.getLogger(ReportedeProduccion.class.getName()).log(Level.SEVERE, null, ex);
                
            }
        }
        
        
    }//GEN-LAST:event_rSButton1ActionPerformed

