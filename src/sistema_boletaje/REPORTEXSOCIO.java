    public void LlenarTabla(){
        PersonalDao PD = new PersonalDao();
        
        ArrayList<Personal> ListarPropietario;
        ListarPropietario = PD.ObtenerPropietario1();
        tb =(DefaultTableModel)tpropietario.getModel();
        ListarPropietario.forEach((obj) -> {
            tb.addRow(new Object[] {obj.getTidentificacion(),obj.getNombre(),obj.getApellidopa(),obj.getApellidoma()});
        });
    }

